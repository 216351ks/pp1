import math

x = 3.7
y = 4

print(f"Pierwiastek kwadratowy z {x} wunosi: {math.sqrt(x)}")

print(f"Pierwiastek stopnia {y} z {x} wynosi: {math.pow(x, 1/y)}")

print(f"{x} do potęgi {y} równa się: {math.pow(x, y)}")

print(f"Pole koła o promieniu {y} wynosi: {math.pi * math.pow(y, 2)}")

print(f"Silnia {y} jest równa: {math.factorial(y)}")

print(f"Największa możliwa liczba całkowita, mniejsza bądź równa {x} wynosi: {math.floor(x)}")